package com.jomap.backend.Entities.Ai;

import org.springframework.boot.CommandLineRunner;
import org.springframework.stereotype.Component;

import java.util.List;

@Component
public class AiDataSeeder implements CommandLineRunner {

    private final AiSuggestedQuestionRepository repository;

    public AiDataSeeder(AiSuggestedQuestionRepository repository) {
        this.repository = repository;
    }

    @Override
    public void run(String... args) throws Exception {
        if (repository.count() == 0) {
            List<String> questions = List.of(
                    "ما هي أفضل الأماكن السياحية في الأردن؟",
                    "كيف يمكنني التسجيل في فعاليات سياحية عبر تطبيق JOMAP؟",
                    "ما هي مميزات تطبيق JOMAP وكيف يساعدني كمسافر؟",
                    "هل يمكنني العثور على عروض ترويجية للفنادق والمطاعم في الأردن؟",
                    "ما هو أفضل وقت لزيارة مدينة البتراء؟",
                    "اقترح لي جدولاً سياحياً لمدة 3 أيام في الأردن.",
                    "كيف يمكن لمنشأتي الانضمام لشبكة شركاء JOMAP؟",
                    "ما هي الأنشطة الترفيهية المتاحة في مدينة العقبة؟",
                    "هل يوفر JOMAP خريطة للوصول للأماكن السياحية؟",
                    "ما هي أشهر الأكلات الشعبية في الأردن وأين يمكنني تجربتها؟",
                    "كيف يعمل نظام الفعاليات في التطبيق؟",
                    "ما هي الأماكن الطبيعية المناسبة للتخييم في الأردن؟",
                    "كيف يمكنني مشاركة تجربتي وتقييم الأماكن داخل التطبيق؟",
                    "ما هي أهم المعالم الأثرية في مدينة جرش؟",
                    "كيف يمكنني التفاعل مع مجتمع السياح في التطبيق؟",
                    "ما هي الفعاليات المتاحة اليوم في العاصمة عمان؟",
                    "اقترح لي مساراً سياحياً لزيارة وادي رم.",
                    "هل تطبيق JOMAP مجاني للاستخدام؟",
                    "ما هي مميزات الحساب التجاري (للمالكين) في التطبيق؟",
                    "أين يمكنني العثور على منتجات يدوية وحرفية في عمان؟",
                    "هل يوجد إشعارات بالعروض والفعاليات الجديدة في التطبيق؟",
                    "ما هي المناطق المناسبة لسياحة المغامرات في الأردن؟",
                    "كيف أستفيد من العروض المتوفرة في قسم التخفيضات؟",
                    "ما هي أشهر المهرجانات التي تقام في الأردن؟",
                    "هل يدعم تطبيق JOMAP اللغتين العربية والإنجليزية؟",
                    "كيف أبحث عن منشآت سياحية في محافظة معينة؟",
                    "اقترح لي أماكن عائلية للترفيه في الأردن.",
                    "هل يمكنني حفظ الأماكن والفعاليات في مفضلتي داخل التطبيق؟",
                    "ما هي إجراءات الأمان والسلامة للسياح في الأردن؟",
                    "كيف أجد وسائل المواصلات العامة بين المحافظات؟",
                    "هل يمكنني إضافة منشأتي السياحية مجاناً على JOMAP؟",
                    "ما هي أفضل الأماكن لالتقاط الصور التذكارية في عمان؟",
                    "كيف يعمل نظام النقاط والمكافآت في التطبيق؟",
                    "ما هي الأماكن المناسبة للزيارة في فصل الشتاء في الأردن؟",
                    "كيف يمكنني التبليغ عن مشكلة واجهتني في التطبيق؟"
            );

            for (String q : questions) {
                AiSuggestedQuestion entity = new AiSuggestedQuestion();
                entity.setQuestion(q);
                entity.setActive(true);
                repository.save(entity);
            }
        }
    }
}
